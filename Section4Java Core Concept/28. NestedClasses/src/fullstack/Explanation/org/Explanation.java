package fullstack.Explanation.org;

public class Explanation {
/*
 Two types of nested classes
 1.static nested classes
 
 In the case of static nested class, Without an outer class object existing, 
 there may be a static nested class object. i.e.,
  an object of a static nested class is not strongly associated with the 
  outer class object.
 
 2. Non static nested classes
 
 In the case of normal or regular inner classes, without an outer class object existing, 
 there cannot be an inner class object. i.e., 
 an object of the inner class is always strongly associated with an outer class object.
 */
}
