
public class breakstatement {
public static void main(String[] args) {
	int c=10;
	switch(c) {
	case 9:
		System.out.println("the value of c is 9");
		break;
	case 10:
		System.out.println("the value of c is 10");
		break;
	case 11:
		System.out.println("the value of c is 11");
		break;
	default:
		System.out.println("the value is other than 11,10,9");
		break;
	}
}
}
