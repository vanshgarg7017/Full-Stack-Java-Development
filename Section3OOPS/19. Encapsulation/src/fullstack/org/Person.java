package fullstack.org;

public class Person {
public String name;
private int age;
private String gender;
public void setName(String name) {
	this.name = name;
}
public void setAge(int age) {
	this.age = age;
}
public void setGender(String gender) {
	this.gender = gender;
}
public String getName() {
	return name;
}
public int getAge() {
	return age;
}
public String getGender() {
	return gender;
}
}
